/* ----------------------- Equal To/Double Equal (==) ----------------------- */
console.log(10 == 10);
console.log(10 == 1);
console.log(10 == "10");
console.log("A" == "a");
console.log("halo" == "halo");
console.log("halo" == "halO");

/* ------------------- Strict Equal To/Triple Equal (===) ------------------- */
console.log(10 === 10);
console.log(10 === 1);
console.log(10 === "10");
console.log("A" === "a");

/* ---------------------------- Greater Than (>) --------------------------- */
console.log(false > 0);

/* ---------------------- Greater Than Or Equal To (>=) --------------------- */
console.log(false >= 0);

/* ------------------------------ Less Than (<) ----------------------------- */
console.log(false < 0);

/* ----------------------- Less Than Or Equal To (<=) ----------------------- */
console.log(false <= 0);

/* ----------------------------- Not Equal (!=) ----------------------------- */
console.log(10 != 10);
console.log(10 != 1);

